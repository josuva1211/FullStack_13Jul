// Assignment Operator

var a = 10;

//Plus Assignment

a += 10;
console.log(a);

var b = 200;

//Subtract Assignment
b -= 100;
console.log(b);

//Multiplication Assignment
var c = 200;
c *= 5;
console.log(c);

//Division Assignment
var d = 5000;
d /= 100;
console.log(d);

//Modulos Assignment
var e = 1234;
e %= 5;
console.log(e);

//Increment Operators

//Post Increment
var f = 1000;
console.log(f++);//1000
console.log(f);//1001
f++;
console.log(f);//1002

//Pre Increment
var g = 500;
console.log(++g);//501
++g;
console.log(g);//502

//Decrement Operators

//Post Decrement

var h = 10;
console.log(h--);//10
console.log(h);//9
h--;
console.log(h);//8

//Pre Decrement

var i = 20;
console.log(--i);//19
--i;
console.log(i);//18